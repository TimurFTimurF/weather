    /** @test */
    public function test_weather_api()
    {
        $url = config('app.weatherapi.forecast') . '?key=' . config('app.weatherapi.key') . '&q=48.866,2.355&days=2';

        $response = Http::get($url);

        $this->assertEquals(200, $response->getStatusCode());
        $this->assertNotEquals(0, count($response->json()));

        $this->assertArrayHasKey('location', $response->json());
        $this->assertArrayHasKey('name', $response->json()['location']);
        $this->assertEquals('Paris', $response->json()['location']['name']);

        $this->assertArrayHasKey('forecast', $response->json());
        $this->assertArrayHasKey('forecastday', $response->json()['forecast']);
        $this->assertCount(2, $response->json()['forecast']['forecastday']);

        $this->assertArrayHasKey('date', $response->json()['forecast']['forecastday'][0]);
        $this->assertEquals(Carbon::now()->format('Y-m-d'), $response->json()['forecast']['forecastday'][0]['date']); //Today
        $this->assertArrayHasKey('day', $response->json()['forecast']['forecastday'][0]);
        $this->assertArrayHasKey('condition', $response->json()['forecast']['forecastday'][0]['day']);
        $this->assertArrayHasKey('text', $response->json()['forecast']['forecastday'][0]['day']['condition']);

        $this->assertArrayHasKey('date', $response->json()['forecast']['forecastday'][1]);
        $this->assertEquals(Carbon::now()->addDay()->format('Y-m-d'), $response->json()['forecast']['forecastday'][1]['date']); //Tomorrow
        $this->assertArrayHasKey('day', $response->json()['forecast']['forecastday'][1]);
        $this->assertArrayHasKey('condition', $response->json()['forecast']['forecastday'][1]['day']);
        $this->assertArrayHasKey('text', $response->json()['forecast']['forecastday'][1]['day']['condition']);
    }

